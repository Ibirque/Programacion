
package proyectos.Nacef;


public class Persona2UF4 {
    //atributos
    private String nombre;
    private String apellido;
    private int edad;
    
    
    //constructor

    public Persona2UF4(String nombre, String apellido, int edad) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
    }
    
    //getters

    public String getNombre() {
        return nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public int getEdad() {
        return edad;
    }
    
    
    
}

